export const getDemoCartStats = () => {
  const total = demoAbandonedCarts.length;
  const totalValue = demoAbandonedCarts.reduce((sum, c) => sum + (c.total_value || 0), 0);
  return {
    total,
    todayCarts: 2,
    totalValue,
    avgValue: total > 0 ? totalValue / total : 0,
    lastCart: demoAbandonedCarts[0]?.synced_at,
  };
};
